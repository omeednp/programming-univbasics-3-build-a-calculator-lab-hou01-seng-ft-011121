def addition(num1, num2)

  sum = num1 + num2
  puts sum

end

def subtraction(num1, num2)

  difference = num2 - num1
  puts difference

end

def division(num1, num2)

  quotient = num1/num2
  puts quotient
  
end

def multiplication(num1, num2)
  
  product = num1 * num2
  puts product
  
end

def modulo(num1, num2)

  modulus = num2%num1
  puts modulus
  
end

def square_root(num)

  root = sqrt(num)
  puts root
  
end